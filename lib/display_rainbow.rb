def display_rainbow(array_colors)
  letters = ["R", "O", "Y", "G", "B", "I", "V"]
  puts "#{letters[0]}: #{array_colors[0]}, 
        #{letters[1]}: #{array_colors[1]},
        #{letters[2]}: #{array_colors[2]},
        #{letters[3]}: #{array_colors[3]},
        #{letters[4]}: #{array_colors[4]},
        #{letters[5]}: #{array_colors[5]},
        #{letters[6]}: #{array_colors[6]}"
end
